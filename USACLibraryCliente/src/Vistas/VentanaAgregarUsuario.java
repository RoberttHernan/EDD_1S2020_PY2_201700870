    private void jButtonRegistrarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarUsuarioActionPerformed
        if (jTextAgregarUsuarioNombre.getText().isEmpty()
                || jTextAgregarUsuarioApellido.getText().isEmpty()
                || jTextAgregarUsuarioCarrera.getText().isEmpty()
                || jTextAgregarUsuarioCarnet.getText().isEmpty()
                || jPasswordRegistrar.getPassword().length == 0) {

        } else {
            String pass = new String(jPasswordRegistrar.getPassword());
            int carnet = Integer.parseInt(jTextAgregarUsuarioCarnet.getText());
            String nombre = jTextAgregarUsuarioNombre.getText();
            String apellido = jTextAgregarUsuarioApellido.getText();
            String carrera = jTextAgregarUsuarioCarrera.getText();

            Estudiante estudiante = new Estudiante(carnet, nombre, apellido, carrera, pass);

            try {
                InetAddress address;
                address = InetAddress.getLocalHost();
                Socket miSocket = new Socket(address, 5050);
                ObjectOutputStream flujoSalida = new ObjectOutputStream(miSocket.getOutputStream());
                flujoSalida.writeByte(5);
                flujoSalida.writeObject(estudiante);
                
                DataInputStream flujoEntrada = new DataInputStream(miSocket.getInputStream());
                String mensaje = flujoEntrada.readUTF();
                JOptionPane.showMessageDialog(rootPane, mensaje);
                
                miSocket.close();
                
                
            } catch (UnknownHostException ex) {
                Logger.getLogger(VentanaAgregarUsuario.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(VentanaAgregarUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButtonRegistrarUsuarioActionPerformed

